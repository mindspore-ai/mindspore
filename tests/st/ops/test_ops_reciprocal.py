from tests.mark_utils import arg_mark
import pytest
import numpy as np
import mindspore as ms
from mindspore import ops
from mindspore.ops.auto_generate.gen_ops_def import Reciprocal

import tests.st.utils.test_utils as test_utils
reciprocal = Reciprocal()

def generate_random_input(shape, dtype):
    return np.random.randn(*shape).astype(dtype)


def generate_expect_forward_output(x):
    return np.reciprocal(x)


def generate_expect_backward_output(x):
    return np.reciprocal(-x*x)


@test_utils.run_with_cell
def reciprocal_forward_func(x):
    return reciprocal(x)


@test_utils.run_with_cell
def reciprocal_backward_func(x):
    return ops.grad(reciprocal_forward_func, (0))(x)


@test_utils.run_with_cell
def reciprocal_vmap_func(x):
    return ops.vmap(reciprocal_forward_func)(x)


@arg_mark(plat_marks=['platform_ascend', 'platform_gpu', 'cpu_linux', 'cpu_windows', 'cpu_macos'], level_mark='level0', card_mark='onecard', essential_mark='essential')
@pytest.mark.parametrize('context_mode', [ms.GRAPH_MODE, ms.PYNATIVE_MODE])
def test_ops_reciprocal_forward(context_mode):
    """
    Feature: pyboost function.
    Description: test function sin forward.
    Expectation: expect correct result.
    """
    ms.context.set_context(mode=context_mode)
    x = generate_random_input((2, 3, 4, 5), np.float32)
    output = reciprocal_forward_func(ms.Tensor(x))
    expect = generate_expect_forward_output(x)
    np.testing.assert_allclose(output.asnumpy(), expect, rtol=1e-3)


